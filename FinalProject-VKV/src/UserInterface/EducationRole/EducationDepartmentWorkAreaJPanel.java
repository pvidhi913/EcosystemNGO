    private void deleteRequestJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteRequestJButtonActionPerformed
       int selectedRow = volunteerRequestJTable.getSelectedRow();
        if(selectedRow >= 0){
            int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to Delete this request??","Warning",selectionButton);
            if(selectionResult == JOptionPane.YES_OPTION){
                EducationVolunteerWorkRequest request = (EducationVolunteerWorkRequest)volunteerRequestJTable.getValueAt(selectedRow, 0);
                
                if(request.getStatus().equals("Completed") || request.getStatus().equals("Pending")){
                    JOptionPane.showMessageDialog(null, "Request can't be deleted since it's already in process!");
                    return;
                }
                
                Organization org = null;
                for(Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
                    if (organization instanceof VolunteerOrganization){
                        org = organization;
                        break;
                    }
                }
                
                if (org!=null){
                    org.getWorkQueue().getWorkRequestList().remove(request);
                    userAccount.getWorkQueue().getWorkRequestList().remove(request);
                } else {
                    JOptionPane.showMessageDialog(null, "Request failed to delete!");
                    return;
                }
                populateRequestTable();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
    }//GEN-LAST:event_deleteRequestJButtonActionPerformed

