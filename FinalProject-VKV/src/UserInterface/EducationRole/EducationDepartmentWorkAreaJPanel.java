    public void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel) volunteerRequestJTable.getModel();
        
        SimpleDateFormat formatter = new SimpleDateFormat("dd MMM, yyyy");
        model.setRowCount(0);
        for (Iterator<WorkRequest> it = userAccount.getWorkQueue().getWorkRequestList().iterator(); it.hasNext();) {
            EducationVolunteerWorkRequest request = (EducationVolunteerWorkRequest) it.next();
            Object[] row = new Object[6];
            row[0] = request;
            Date date = request.getDate();
            String volDate = formatter.format(date);
            row[1] = volDate;
            row[2] = request.getTime();
            row[3] = request.getNumberOfVolunteers(); 
            row[4] = request.getCurrentVacancy();
            row[5] = request.getStatus();
            model.addRow(row); 
        }
    }

