package Business.Role;

import Business.EcoSystem;
import Business.Enterprise.Enterprise;
import Business.Network.Network;
import Business.Organization.Organization;
import Business.UserAccount.UserAccount;
import javax.swing.JPanel;

/**
 *
 * @author vaibhaveegamit
 */
public abstract class Role {
    
    public enum RoleType{
        
        SystemAdmin("System Admin Role"),
        Admin("Admin Role"),
        Analyst("Analyst Role"),
        Doctor("Doctor Role"),
        Education("Education Role"),
        Health("Health Role"),
        Inventory("Inventory Role"),
        Pharmacy("Pharmacy Role"),
        Transport("Transport Role"),
        Volunteer("Volunteer Role");
        
        private String value;
        
        private RoleType(String value){
            this.value = value;
        }

        public String getValue() {
            return value;
        }

        @Override
        public String toString() {
            return value;
        }
    }
    
    public abstract JPanel createWorkArea(
            JPanel userProcessContainer, 
            UserAccount account, 
            Organization organization, 
            Enterprise enterprise,
            Network network,
            EcoSystem business);

    @Override
    public String toString() {
        return this.getClass().getName();
    }
    
}
